export async function GET() {
  const session = await getServerSession(authOptions)
  if (!session?.user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 })

  const sb = adminClient()

  const { data: existing } = await sb
    .from("lessons")
    .select("id, title")
    .eq("title", LESSON_TITLE)
    .maybeSingle()

  if (existing) {
    return NextResponse.json({ message: "Lesson already exists", id: existing.id, title: existing.title })
  }

  const slug = `civics-nation-state-law-3`
  const { data, error } = await sb
    .from("lessons")
    .insert({
      title: LESSON_TITLE,
      subject: "אזרחות",
      slug,
      slides,
    })
    .select()
    .single()

  if (error) return NextResponse.json({ error: error.message }, { status: 500 })
  return NextResponse.json({ message: "Lesson created", id: data.id, title: data.title, slideCount: slides.length })
}
